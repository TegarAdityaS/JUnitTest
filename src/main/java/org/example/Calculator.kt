package org.example

// kelas yang digunakan untuk melakukan operasi matematika dasar
class Calculator {

    // Digunakan untuk menambahkan dua angka
    fun add(first: Int, second: Int): Int {
        return first + second
    }

    // Digunakan untuk mengurangkan dua angka
    fun sub(first: Int, second: Int): Int {
        return first - second
    }
}